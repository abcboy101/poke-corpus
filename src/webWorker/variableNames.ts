export const variableNameToCode = (variableName: string) => {
  for (const [code, name] of variables3DS)
    if (name === variableName)
      return code;
  return undefined;
};

export const variableCodeToName = (variableCode: string) => {
  for (const [code, name] of variables3DS)
    if (code === variableCode)
      return name;
  return undefined;
};
